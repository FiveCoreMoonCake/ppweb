// Generate TypeScript
let ts = `export interface CharReading {
  pinyin: string;
  words: string[];
  emoji: string;
}

export interface CharItem {
  char: string;
  readings: CharReading[];
  groupId: string;
}

export interface CharGroup {
  id: string;
  name: string;
  chars: CharItem[];
}

`;

const periodNames = [
  "第1期", "第2期", "第3期", "第4期",
  "第5期", "第6期", "第7期", "第8期",
];

for (let pi = 0; pi < periods.length; pi++) {
  const gid = `p${pi + 1}`;
  ts += `const ${gid}: CharItem[] = [\n`;
  for (const entry of periods[pi]) {
    const char = entry[0];
    const data = entry[1];
    if (Array.isArray(data)) {
      // Polyphonic
      ts += `  { char: "${char}", readings: [\n`;
      for (const r of data) {
        ts += `    { pinyin: "${r[0]}", words: ["${r[1]}", "${r[2]}"], emoji: "${r[3]}" },\n`;
      }
      ts += `  ], groupId: "${gid}" },\n`;
    } else {
      // Single reading
      const pinyin = entry[1];
      const w1 = entry[2];
      const w2 = entry[3];
      const emoji = entry[4];
      ts += `  { char: "${char}", readings: [{ pinyin: "${pinyin}", words: ["${w1}", "${w2}"], emoji: "${emoji}" }], groupId: "${gid}" },\n`;
    }
  }
  ts += `];\n\n`;
}

